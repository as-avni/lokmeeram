import { collection, getDocs } from "firebase/firestore";
import { db } from "./firebase";

export async function fetchBlogs() {
  const snapshot = await getDocs(collection(db, "blogs"));
  return snapshot.docs.map(doc => ({ id: doc.id, ...doc.data() }));
}